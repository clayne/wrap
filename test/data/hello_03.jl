#=
   Julia is a high-level dynamic programming language designed to address the requirements of high-performance numerical and scientific computing while also being effective for general-purpose programming, web use or as a specification language.
 
   Distinctive aspects of Julia's design include a type system with parametric types in a fully dynamic programming language and multiple dispatch as its core programming paradigm.
   It allows concurrent,
   parallel and distributed computing,
   and direct calling of C and Fortran libraries without glue code.
   Julia is garbage-collected,
   uses eager evaluation and includes efficient libraries for floating-point calculations,
   linear algebra, random number generation, fast Fourier transforms and regular expression matching.
